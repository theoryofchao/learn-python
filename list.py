test_list = ['test1', 'test2', 'test3']
## print the list
print(test_list)

## print item at index 0
print(test_list[0])

## append more items to test_list
test_list.append('test4')
print(test_list)

## pop last item
test_list.pop()
print(test_list)

## pop first item
test_list.pop(0)
print(test_list)

## print length of list
print(len(test_list))

## print length of string
print(len("string"))